function convertTamilFont(string){
    var lines = string.split(/\r?\n/);
    console.log(lines);
    var res = lines.map(convertSingleLine);
    return(res.join(' \n<br>'));
}


function convertSingleLine(line){
    var regex = /ஸ்ரீ|க்ஷெ|க்ஷே|க்ஷொ|க்ஷோ|க்ஷௌ|க்ஷா|க்ஷ்|க்ஷி|க்ஷீ|க்ஷு|க்ஷூ|க்ஷ|[அஆஇஈஉஊஎஏஐஒஓஔஃ]|[கஙசஞடணதநபமயரலவழளறனஷஜஸஹ][்ாிீுூெேொோைௌ]?|.|/g;
    var splitted = line.match(regex);
    var res = splitted.map(convertSingleEntity);
    return(res.join(''));
}

function convertSingleEntity(ent){
    console.log(ent)
    if (unic2stmzh.hasOwnProperty(ent)){
        return unic2stmzh[ent];
    }
    return ent;
}
